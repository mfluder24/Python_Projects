class Animal:
    # Define attributes of the the parent class
    name = ""
    species = ""
    diet = ""
    age = 0

class Crow(Animal):
    # Define attributes of the the child class
    flySpeed = 42
    canSpeak = True

class RainbowTrout(Animal):
    # Define attributes of the the child class
    swimSpeed = 4
    finRot = False
    
